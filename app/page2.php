    <h3>SEJARAH SINGKAT</h3><br>
    <P>PP AL-ANKABUUT didirikan oleh Pangersa Abah KH.SHOLEHUDIN. Beliau Lahir di Bekasi, tanggal 25 September 1983. Beliau pernah sekolah di Madrasah Al-Ridwan Bekasi, dan beberapa majelis ilmu agama lainnya dengan berbagai ustadz sampai dengan lulus sekolah Dasar.

Hampir seluruh massa kecil beliau tak lepas dari lingkungan religi terkhusus dari Ayahanda beliau Al-Ustadz Alwy bin Abdullah Al-Haddad dan Kakek beliau Al-Habib Muhammad bin Muhsin Al-Athas yang berasal dari wilayah Huraidah, Hardramaut – Yaman Selatan. Seluruh pituah mulia dan nasihat dari kakek-nenek, guru dan lingkungan beliau sangat mempengaruhi semangat beliau dalam berdakwah khususnya sebagai salah seorang cicit Rasulullah SAW dari jalur Ali Bin Ali Thalib dan Fatimah Az-Zahra untuk berdakwah di Nusantra – Indonesia.

Selesai pendidikan dari Pondok Pesantren Darul Lughah Waddaa`wah (DALWA) Bangil-Jawa Timur yang dipimpin oleh Al-Habib Al-Ustadz Hasan bin Ahmad Bahraun; Pangersa Abah KH.SHOLEHUDIN melanjutkan dakwah yang telah dilakukan oleh Ayahanda beliau di Desa Pabuaran Sukamakmur – Bogor hingga akhirnya dapat menghadirkan Pondok Pesantren DILWA pada tahaun 2016 silam.</P>
