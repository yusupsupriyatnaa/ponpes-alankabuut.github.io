                  <h3>PROFIL PP AL ANKABUUT</h3><br>
    <p>Pondok Pesantren Al-Ankabuut adalah satu lembaga dakwah Islam yang bergerak dalam berbagai bidang kemasyarakatan yang berlokasi di alamat Perum Sukasenang Kecamatan Banyuresmi Kabupaten Garut, Jawa Barat.

Beberapa program Pesantren Al Ankabuut saat ini adalah: Majelis Dalailul Khoirot ,kegiatan pendidikan ilmu keagamaan melalui kurikulum pondok posantren mandiri dan terpadu yang sudah berjalan semenjak tahun 2014, program tahfiz ilmu Al-quran dan pengumpulan zakat, infak dan sedekah internal, program ekonomi syariah dan kegiatan sosial lainnya yang menyebar diberbagai wilayah khususnya di Jabodetabek, dan Jawa Barat lainnya.

Sejak beridirinya telah menjalani berbagai dinamika, berbagai kegiatan dijalankan dalam berbagai keterbatasan baik sumber daya manusia, sumber dana dan energi lainnya. Sebagian besar program dijalankan secara swadaya dibawah pimpinan Pangersa Abah KH.SHOLEHUDIN dan kader dakwah yang sudah terbina secara baik. </p>


